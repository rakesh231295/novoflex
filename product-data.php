function novoflex_product_url($slug) {
  global $novoflexProducts;

  if (!empty($novoflexProducts[$slug]['direct_subcategory'])) {
    $categorySlug = array_key_first($novoflexProducts[$slug]['categories']);
    return novoflex_category_url($slug, $categorySlug);
  }

  return 'category.php?product=' . urlencode($slug);
}

function novoflex_category_url($productSlug, $categorySlug) {
  return 'sub-categories.php?product=' . urlencode($productSlug) . '&category=' . urlencode($categorySlug);
}

function novoflex_item_title($item) {
  return is_array($item) ? $item['title'] : $item;
}

function novoflex_item_image($item, $category, $product) {
  if (is_array($item) && !empty($item['image'])) {
    return $item['image'];
  }

  if (!empty($category['image'])) {
    return $category['image'];
  }

  return $product['image'];
}
