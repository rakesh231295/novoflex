document.addEventListener('DOMContentLoaded', function () {
    const card = document.querySelector('.kwiklok-spec-card');
    if (!card) return;

    const rows = Array.from(card.querySelectorAll('tbody tr'));
    const trigger = card.querySelector('.quote-request-trigger');
    const form = card.querySelector('.quote-spec-form');
    const back = card.querySelector('.quote-form-back');
    const summary = card.querySelector('.quote-spec-summary');
    const comment = form ? form.querySelector('textarea') : null;

    const getSelections = () => rows
        .map((row) => {
            const checkbox = row.querySelector('input[type=\"checkbox\"]');
            const qty = row.querySelector('.quote-spec-qty');
            const part = checkbox ? checkbox.getAttribute('data-part') : '';
            return checkbox && checkbox.checked ? `${part} - Qty: ${qty.value || 1}` : null;
        })
        .filter(Boolean);

    const syncState = () => {
        const selected = getSelections();
        trigger.disabled = selected.length === 0;
        if (summary) {
            summary.textContent = selected.length
                ? `Selected items: ${selected.join(', ')}`
                : 'Selected items will appear here after choosing quantity and checkbox.';
        }
        if (comment && selected.length) {
            comment.value = `Selected products: ${selected.join(', ')}`;
        }
    };

    rows.forEach((row) => {
        const checkbox = row.querySelector('input[type=\"checkbox\"]');
        const qty = row.querySelector('.quote-spec-qty');
        if (!checkbox || !qty) return;

        checkbox.addEventListener('change', function () {
            if (checkbox.checked && !qty.value) qty.value = 1;
            syncState();
        });

        qty.addEventListener('input', function () {
            if (qty.value && Number(qty.value) > 0) checkbox.checked = true;
            if (!qty.value) checkbox.checked = false;
            syncState();
        });
    });

    trigger.addEventListener('click', function () {
        form.classList.add('is-visible');
        syncState();
        form.scrollIntoView({ behavior: 'smooth', block: 'start' });
    });

    back.addEventListener('click', function () {
        form.classList.remove('is-visible');
    });

    syncState();
});
</script>

